function parseGrid(filepath) {

    let text = read(filepath)
    let lines = text.split("\n")
    let numRows = lines[0].split(" ").pop()
    let numCols = lines[1].split(" ").pop()
    let arr = []

    for(let i = 2; i < lines.length; i++){
        arr.push(lines[i].replaceAll('[', '')
            .replaceAll(']', '')
            .replaceAll(',', '')
            .split(" "))
    }
    arr.pop()

    let grid = new Grid(numRows, numCols, arr);
    return grid;
}